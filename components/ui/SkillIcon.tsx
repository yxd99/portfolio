export function SkillIcon({ iconId, name, className }: Props) {
  const [useFallback, setUseFallback] = useState(false);
  const normalizedSlug = iconId.toLowerCase().replace(/\./g, 'dot');
  const cdnSlug = CDN_SLUG_MAP[normalizedSlug] ?? normalizedSlug;
  const inlinePath = INLINE_ICONS[normalizedSlug];

  if (useFallback || inlinePath) {
    if (inlinePath) {
      return (
        <svg
          role="img"
          viewBox="0 0 24 24"
          className={className}
          aria-hidden
          fill="currentColor"
        >
          <path d={inlinePath} />
        </svg>
      );
    }
    return (
      <span
        className={`flex items-center justify-center rounded bg-muted/50 text-[10px] font-semibold text-foreground ${className ?? ''}`}
        aria-hidden
      >
        {name ? name.slice(0, 2).toUpperCase() : iconId.slice(0, 2).toUpperCase()}
      </span>
    );
  }

  return (
    <img
      src={`https://cdn.simpleicons.org/${cdnSlug}`}
      alt=""
      width={24}
      height={24}
      loading="lazy"
      className={className}
      onError={() => setUseFallback(true)}
    />
  );
}
